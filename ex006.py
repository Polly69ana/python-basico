# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

# Ler o número do usuário
n = int(input('Um valor: '))

# O dobro
d = n * 2

# O triplo
t = n * 3

# Raiz quadrada
r = n ** 0.5

print(f'O dobro de {n}, vale {d}\nO triplo de {n} vale {t}\nA raiz quadrada de {n} é igual {r}.')